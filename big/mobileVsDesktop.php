<h1>BIG Project Research #1: Responsive vs Mobile</h1>

<p>Barbara Pronsato,  2/1/2020 </p>


<p>The importance for a  company to analyse their best return on investment when deciding on whether to go with a mobile application or just a responsive site should not 
be underestimated.  They may want to take into consideration costs, user experience and the industry that their in. A responsive website is less expensive and will take 
less time to develop and maintain. There are no constraints with the version of mobile device they are being installed on and there is no approval process.
On the other hand they may have a hard time accessing the camera, microphone, location services and other device internal features, costs can become a concern when 
aving to support multiple platforms, users are not guaranteed safety and security because there is no quality control.
Mobile apps are faster than web apps, they have access to the camera etc and more secure. Better customer support and experience. Easy to find for user. Conversely, 
osts for maintaining and updating can be higher, especially with having to maintain and update two mobile platforms. The approval process can be a barrier for some 
developers, the different versions that are out in the field offer a challenge for support and maintenance. 
Industries that benefit most from native apps, are larger companies with widespread brand recognition such as Yelp, ebay, Amazon or Nintendo. Subscription services, s
uch as health and wellness apps that are pretty much “standalone” also benefit greatly from developing an excellent user experience and could not survive without p
roviding a native mobile service. Whereas, a work portfolio or a blog might not want to focus on building and developing an app that would add little value for their users. 
With a few exceptions, such as a meditation app or a game, most likely a business will have to create a responsive site and if there is extra investment capital then a mobile 
app would be a great service to provide. With a few exceptions, most businesses will start out with a responsive site and move on to develop a mobile app eventually or in 
tandem.</p>


